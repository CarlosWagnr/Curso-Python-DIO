# Listas em Python podem armazenar de maneira sequencial qualquer tipo de de objeto.
# Podemos usar o construtor list, a função range ou colocando valores separados por vírgulas dentro de colchetes.
# Listas são objetos multáveis, portanto podmeos alterar seus valores após a criação.
frutas = ["laranja", "maca", "uva"]
print(frutas)

frutas = []
print(frutas)

letras = list("python")
print(letras)

numeros = list(range(10))
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
