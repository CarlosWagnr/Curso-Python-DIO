print("###### Tipos de dados em Python ######")
print("Tipos Numéricos - Inteiro")
# Números Inteiros são representados pela classe int e possuem precisão limitada.
# EX: 1, 10, 100, -1, 98
print(1, 10, 100, -1, 98)
print(11 + 10 + 1000) 
print()

print("Tipos Numéricos - Flutuante")
# Números de Ponto Flutuante são usados para representados para representar números racionais
# e sua implementação é feita  pela classe float.
# EX: 1.5, -10.543, 0.76
print(1.5, -10.543, 0.76)
print(1.5 + 0.5)
print()

print("Tipo Booleano")
# É usado para representar verdadeiro ou falso, e é implementado pela classe bool.
# EX: True e False
print(True, False)
print()

print("Tipo Strings")
# É usado para representar valores alfanuméricos, e é implementado pela classe str.
# EX: "Python", 'Python', """Python"""
print("Python")

